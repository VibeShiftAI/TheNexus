#!/bin/bash
# в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ
#  THE NEXUS вЂ” Environment Configuration
#  Sets API keys in both .env and nexus-builder/.env
# в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ

NEXUS_DIR="$(cd "$(dirname "$0")" && pwd)"
ROOT_ENV="$NEXUS_DIR/.env"
PYTHON_ENV="$NEXUS_DIR/nexus-builder/.env"

echo ""
echo "  в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—"
echo "  в•‘        THE NEXUS вЂ” Environment Setup                  в•‘"
echo "  в• в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•Ј"
echo "  в•‘  This will configure your API keys.                   в•‘"
echo "  в•‘  Press ENTER to keep the current value (shown in      в•‘"
echo "  в•‘  brackets). API keys are written to both .env files.  в•‘"
echo "  в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ"
echo ""

# Ensure .env files exist
if [ ! -f "$ROOT_ENV" ]; then
    if [ -f "$NEXUS_DIR/.env.example" ]; then
        cp "$NEXUS_DIR/.env.example" "$ROOT_ENV"
        echo "  Created .env from .env.example"
    else
        echo "  ERROR: .env.example not found. Run the installer first."
        exit 1
    fi
fi

if [ ! -f "$PYTHON_ENV" ]; then
    if [ -f "$NEXUS_DIR/nexus-builder/.env.example" ]; then
        cp "$NEXUS_DIR/nexus-builder/.env.example" "$PYTHON_ENV"
        echo "  Created nexus-builder/.env from .env.example"
    fi
fi

# в”Ђв”Ђ Helper: read a key from an env file в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
read_env_val() {
    local file="$1" key="$2"
    grep -E "^${key}=" "$file" 2>/dev/null | head -1 | cut -d'=' -f2-
}

# в”Ђв”Ђ Read current values в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
CUR_PROJECT_ROOT=$(read_env_val "$ROOT_ENV" "PROJECT_ROOT")
CUR_GOOGLE=$(read_env_val "$ROOT_ENV" "GOOGLE_API_KEY")
CUR_OPENAI=$(read_env_val "$ROOT_ENV" "OPENAI_API_KEY")
CUR_ANTHROPIC=$(read_env_val "$ROOT_ENV" "ANTHROPIC_API_KEY")
CUR_XAI=$(read_env_val "$ROOT_ENV" "XAI_API_KEY")

# в”Ђв”Ђ Prompt helper в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
prompt_val() {
    local label="$1" current="$2" result
    if [ -n "$current" ]; then
        read -r -p "  $label [$current]: " result
        echo "${result:-$current}"
    else
        read -r -p "  $label: " result
        echo "$result"
    fi
}

# в”Ђв”Ђ Prompt for values в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo "  в”Ђв”Ђ Project Root в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ"
echo "  The folder containing your coding projects."
NEW_PROJECT_ROOT=$(prompt_val "PROJECT_ROOT" "$CUR_PROJECT_ROOT")
echo ""

echo "  в”Ђв”Ђ API Keys (at least one required) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ"
echo ""
NEW_GOOGLE=$(prompt_val "GOOGLE_API_KEY" "$CUR_GOOGLE")
NEW_ANTHROPIC=$(prompt_val "ANTHROPIC_API_KEY" "$CUR_ANTHROPIC")
NEW_OPENAI=$(prompt_val "OPENAI_API_KEY" "$CUR_OPENAI")
NEW_XAI=$(prompt_val "XAI_API_KEY" "$CUR_XAI")
echo ""

# в”Ђв”Ђ Write root .env в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo "  Writing .env ..."

cat > "$ROOT_ENV" << EOF
# ==============================================================================
# TheNexus Environment Variables
# ==============================================================================

# --- Server ---
PORT=4000
PROJECT_ROOT=${NEW_PROJECT_ROOT}

# --- AI Provider API Keys ---
GOOGLE_API_KEY=${NEW_GOOGLE}
OPENAI_API_KEY=${NEW_OPENAI}
ANTHROPIC_API_KEY=${NEW_ANTHROPIC}
XAI_API_KEY=${NEW_XAI}

# --- Frontend URLs ---
NEXT_PUBLIC_API_URL=http://localhost:4000
NEXT_PUBLIC_CORTEX_URL=http://localhost:8000
EOF

echo "    вњ“ .env updated"

# в”Ђв”Ђ Write nexus-builder .env в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo "  Writing nexus-builder/.env ..."

cat > "$PYTHON_ENV" << EOF
# ==============================================================================
# LangGraph Engine Environment Variables
# ==============================================================================

# AI Provider API Keys
GOOGLE_API_KEY=${NEW_GOOGLE}
ANTHROPIC_API_KEY=${NEW_ANTHROPIC}
OPENAI_API_KEY=${NEW_OPENAI}

# Node.js backend URL (for proxying requests)
NODEJS_BACKEND_URL=http://localhost:4000
EOF

echo "    вњ“ nexus-builder/.env updated"

echo ""
echo "  в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—"
echo "  в•‘         Configuration saved!                          в•‘"
echo "  в•‘                                                       в•‘"
echo "  в•‘   Double-click \"Start The Nexus.command\"              в•‘"
echo "  в•‘   or run: ./start-local.sh                            в•‘"
echo "  в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ"
echo ""
